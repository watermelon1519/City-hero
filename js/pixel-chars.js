// 渲染像素角色到 Canvas
class PixelCharRenderer {
  constructor() {
    this.cache = new Map();
  }

  // 渲染角色到 canvas
  render(charId, frame = 'idle', scale = 2) {
    const char = PIXEL_CHARS[charId] || PIXEL_ENEMIES[charId];
    if (!char) return null;

    const frameData = char.frames[frame] || char.frames.idle;
    if (!frameData) return null;

    const size = 32;
    const canvas = document.createElement('canvas');
    canvas.width = size * scale;
    canvas.height = size * scale;
    const ctx = canvas.getContext('2d');

    const colors = char.colors;
    
    for (let y = 0; y < size; y++) {
      for (let x = 0; x < size; x++) {
        const idx = parseInt(frameData[y * size + x], 10);
        if (idx > 0 && colors[idx]) {
          ctx.fillStyle = colors[idx];
          ctx.fillRect(x * scale, y * scale, scale, scale);
        }
      }
    }

    return canvas;
  }

  // 创建动画精灵
  createSprite(charId, scale = 2) {
    const char = PIXEL_CHARS[charId] || PIXEL_ENEMIES[charId];
    if (!char) return null;

    const container = document.createElement('div');
    container.className = 'pixel-sprite';
    container.style.width = `${32 * scale}px`;
    container.style.height = `${32 * scale}px`;
    container.style.imageRendering = 'pixelated';

    const canvas = this.render(charId, 'idle', scale);
    if (canvas) {
      container.appendChild(canvas);
    }

    return container;
  }

  // 创建动画（空闲动画）
  createAnimatedSprite(charId, scale = 2, fps = 2) {
    const char = PIXEL_CHARS[charId] || PIXEL_ENEMIES[charId];
    if (!char) return null;

    const frames = Object.keys(char.frames);
    if (frames.length === 0) return null;

    const container = document.createElement('div');
    container.className = 'pixel-sprite animated';
    container.style.width = `${32 * scale}px`;
    container.style.height = `${32 * scale}px`;
    container.style.imageRendering = 'pixelated';
    container.style.position = 'relative';

    let currentFrame = 0;
    const canvas = this.render(charId, frames[0], scale);
    if (canvas) {
      container.appendChild(canvas);
    }

    // 如果有多帧，创建动画
    if (frames.length > 1) {
      setInterval(() => {
        currentFrame = (currentFrame + 1) % frames.length;
        const newCanvas = this.render(charId, frames[currentFrame], scale);
        if (newCanvas) {
          container.innerHTML = '';
          container.appendChild(newCanvas);
        }
      }, 1000 / fps);
    }

    return container;
  }
}

